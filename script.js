let currentCat = "شخصي";
let currentQIndex = 0;

// دالة التعليق الصوتي
function speak(text) {
    window.speechSynthesis.cancel(); // إيقاف أي صوت حالي
    const utterance = new SpeechSynthesisUtterance(text);
    utterance.lang = 'ar-SA'; // اللغة العربية
    utterance.rate = 0.9; // سرعة الكلام (طبيعية)
    window.speechSynthesis.speak(utterance);
}

function changeCategory(cat, element) {
    currentCat = cat;
    document.querySelectorAll('.nav-btn').forEach(btn => btn.classList.remove('active'));
    element.classList.add('active');
    const labels = {'شخصي': 'الجانب الشخصي', 'بيداغوجيا': 'البيداغوجيا', 'تخصص رياضيات': 'التخصص', 'مواقف': 'المواقف وأنواع التلاميذ'};
    document.getElementById('current-category-label').innerText = `الوضع الحالي: ${labels[cat]}`;
    getNewQuestion();
}

function getNewQuestion() {
    const questions = allQuestions[currentCat];
    currentQIndex = Math.floor(Math.random() * questions.length);
    const questionText = questions[currentQIndex].q;
    document.getElementById('question-box').innerText = questionText;
    document.getElementById('user-input').value = "";
    document.getElementById('feedback-area').innerHTML = "";
    
    // نطق السؤال الجديد تلقائياً
    speak(questionText);
}

function checkResponse() {
    const userInput = document.getElementById('user-input').value.trim();
    const feedback = document.getElementById('feedback-area');
    const correctData = allQuestions[currentCat][currentQIndex];
    
    if (userInput.length < 10 || userInput.includes("لا اعرف") || userInput.includes("لا أعرف")) {
        const errorMsg = "تنبيه. الإجابة غير كافية. الإجابة النموذجية هي: " + correctData.a;
        feedback.innerHTML = `
            <div class="lcd-box lcd-error">
                <span class="lcd-title">SYSTEM STATUS: FAILED 🔴</span>
                <div class="lcd-result">
                    <p><strong>تنبيه:</strong> الإجابة مفقودة أو تفتقر للمنهجية.</p>
                    <div class="model-ans"><strong>النموذج:</strong> ${correctData.a}</div>
                </div>
            </div>`;
        speak(errorMsg);
        return;
    }

    let matches = 0;
    correctData.key.forEach(word => { if (userInput.includes(word)) matches++; });

    if (matches >= 1) {
        const successMsg = "تم قبول الإجابة. إليك النموذج الكامل: " + correctData.a;
        feedback.innerHTML = `
            <div class="lcd-box lcd-success">
                <span class="lcd-title">SYSTEM STATUS: AUTHORIZED 🔵</span>
                <div class="lcd-result">
                    <p><strong>تحليل:</strong> تم اكتشاف مصطلحات دقيقة.</p>
                    <div class="model-ans"><strong>النموذج الكامل:</strong> ${correctData.a}</div>
                </div>
            </div>`;
        speak(successMsg);
    } else {
        const lowMsg = "تحليل النظام يشير إلى نقص في المصطلحات. الإجابة الصحيحة هي: " + correctData.a;
        feedback.innerHTML = `
            <div class="lcd-box lcd-error">
                <span class="lcd-title">SYSTEM STATUS: INVALID DATA 🟡</span>
                <div class="lcd-result">
                    <p><strong>تحليل:</strong> الصياغة تفتقر للكلمات المفتاحية.</p>
                    <div class="model-ans"><strong>الإجابة الصحيحة:</strong> ${correctData.a}</div>
                </div>
            </div>`;
        speak(lowMsg);
    }
}